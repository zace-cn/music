package com.zdy.imusic.entity;

/**
 * @author zdy
 *
 */
public class VoiceAnimParam {
	private float radius;
	private int alpha;

	public VoiceAnimParam(float radius, int alpha) {
		this.radius = radius;
		this.alpha = alpha;
	}

	public float getRadius() {
		return radius;
	}

	public void setRadius(float radius) {
		this.radius = radius;
	}

	public int getAlpha() {
		return alpha;
	}

	public void setAlpha(int alpha) {
		this.alpha = alpha;
	}

	
}
